    public String get(String pName) {
        String result;
        String mark;
        if (this.param == null) {
            return null;
        }
        if (this.param.get(pName) != null) {
            result = this.param.get(pName);
        } else {
            result = "";
        }
        if (result.trim().isEmpty() && this.defaultParam.containsKey(pName)) {
            result = this.defaultParam.get(pName);
        }
        if (result != null && !result.trim().isEmpty()) {
            String[] temp = result.split("\\|");
            StringBuffer sb = new StringBuffer();
            Map map2 = this.param_b;
            if (map2.containsKey(this.locale)) {
                mark = map2.get(this.locale);
            } else {
                mark = "\\b";
            }
            String mark2 = mark;
            for (String t : temp) {
                if (!t.trim().isEmpty()) {
                    if (t.equals(SAVE_OR)) {
                        sb.append("|");
                    } else if (t.equals(WITHNOT_B)) {
                        mark2 = "";
                    } else {
                        if (!t.startsWith("\\b")) {
                            sb.append(mark2);
                        }
                        sb.append(t);
                        if (!t.endsWith("\\b") && !t.endsWith(".")) {
                            sb.append(mark2);
                        }
                        sb.append("|");
                    }
                }
            }
            result = sb.toString();
            if (result.endsWith("|")) {
                result = result.substring(0, result.length() - 1);
            }
        }
        return result;
    }

    public String getWithoutB(String pName) {
        if (this.param == null) {
            return null;
        }
        String result = this.param.get(pName) != null ? this.param.get(pName) : "";
        if (result.indexOf(WITHNOT_B) != -1) {
            String[] temp = result.split("\\|");
            StringBuffer sb = new StringBuffer();
            for (String t : temp) {
                if (!t.trim().isEmpty() && !t.equals(SAVE_OR) && !t.equals(WITHNOT_B)) {
                    sb.append(t);
                    sb.append("|");
                }
            }
            result = sb.toString();
            if (result.endsWith("|")) {
                result = result.substring(0, result.length() - 1);
            }
        }
        return result;
    }

